// for loop
for (let i = 0; i < 5; i++){
  console.log(`This is loop no ${i}`);
}

// for of (forr array)
let numbers = [3, 6, 9,12, 4, 1, 67, 2];
for (number of numbers){
  console.log(`number : ${number}`);
}

// while loop
let j = 1;
while(j < 6){
  console.log(`This is while loop no ${j}`);
  j++;
}

// do while
let k = 10;
do{
  console.log(`This is do while loop no ${k}`);
  k++;
} while(k < 8);